  CUST_KFT DOUBLE NULL,
  CUST_TFT DOUBLE NULL,
  CUST_CTU DOUBLE NULL,
  CUST_SM DOUBLE NULL,
  PRIMARY KEY (`ID_CUSTO`))

CREATE TABLE TB_IMPOSTO (
  ID_IMPOSTO INT NOT NULL,
  IMP_MC DOUBLE,
  IMP_TX DOUBLE,
  PRIMARY KEY (`ID_IMPOSTO`))

CREATE TABLE TB_TEMPO (
  ID_TEMPO INT,
  TMP_TC DOUBLE,
